# CELL 23: CASH FLOW QUALITY — EARNINGS QUALITY ANALYSIS
#
# The "Earnings Quality" check compares:
#   Net Income (what accountants report) vs.
#   Operating Cash Flow (what actually hit the bank)
#
# HIGH QUALITY EARNINGS: OCF ≥ Net Income
# LOW QUALITY EARNINGS:  OCF << Net Income
#   → Company may be using accounting tricks to inflate profits
#
# A company can be "profitable" on paper but burning cash.
# Enron was "profitable" right until it collapsed.
# Apple's OCF consistently EXCEEDS Net Income — elite quality.
# ============================================================

def cashflow_quality_analysis(analyzer):
    """Analyze earnings quality by comparing OCF to Net Income."""

    print("=" * 70)
    print("         APPLE INC. — EARNINGS QUALITY ANALYSIS")
    print("=" * 70)

    periods = analyzer.periods_sorted
    period_labels = [p.strftime('%b %Y') for p in periods]

    net_incomes   = [analyzer.safe_get(analyzer.income,
                      ['Net Income', 'Net Income Common Stockholders'], p)
                     for p in periods]
    op_cfs        = [analyzer.safe_get(analyzer.cashflow,
                      ['Operating Cash Flow',
                       'Cash Flow From Continuing Operating Activities'], p)
                     for p in periods]
    capex_list    = [abs(analyzer.safe_get(analyzer.cashflow,
                      ['Capital Expenditure',
                       'Purchase Of Property Plant And Equipment'], p))
                     for p in periods]
    fcf_list      = [ocf - cap for ocf, cap in zip(op_cfs, capex_list)]

    # Earnings quality ratio = OCF / Net Income
    # > 1.0 = High quality  |  < 1.0 = Low quality
    quality_ratios = [(ocf / ni) if ni > 0 else 0
                      for ocf, ni in zip(op_cfs, net_incomes)]

    header = f"\n  {'Metric':<30}" + "".join(f"  {lbl:>10}" for lbl in period_labels)
    print(header)
    print("  " + "─" * (30 + 12 * len(periods)))

    rows_data = [
        ('💵 Net Income ($B)',         net_incomes,    '${:.1f}B'),
        ('🏦 Operating CF ($B)',        op_cfs,         '${:.1f}B'),
        ('🔧 CapEx ($B)',               capex_list,     '-${:.1f}B'),
        ('⭐ Free Cash Flow ($B)',      fcf_list,       '${:.1f}B'),
        ('📊 Quality Ratio (OCF/NI)',   quality_ratios, '{:.2f}x'),
    ]

    for label, values, fmt in rows_data:
        row = f"  {label:<30}"
        for v in values:
            try:
                row += f"  {fmt.format(v):>10}"
            except:
                row += f"  {'N/A':>10}"
        print(row)

    # Quality assessment
    latest_ratio = quality_ratios[-1] if quality_ratios else 0
    print(f"\n  {'─'*60}")
    print(f"\n  EARNINGS QUALITY VERDICT (Most Recent Year):")
    if latest_ratio >= 1.2:
        verdict = "🟢 EXCELLENT — OCF significantly exceeds Net Income"
    elif latest_ratio >= 1.0:
        verdict = "✅ GOOD     — OCF matches or exceeds Net Income"
    elif latest_ratio >= 0.75:
        verdict = "🟡 FAIR     — Some gap; investigate further"
    else:
        verdict = "🔴 POOR     — Net Income may be overstated vs. reality"

    print(f"  Quality Ratio: {latest_ratio:.2f}x  →  {verdict}")

    print(f"""
  📌 WARREN BUFFETT'S TEST:
  "I look for businesses that generate more cash
   than they consume." Apple passes this test every year.

  FCF YIELD = Free Cash Flow ÷ Market Cap
  = ~$110B FCF ÷ $4,530B Market Cap = ~2.4%

  Compare to a 10-year US Treasury bond (~4.3%).
  Apple's FCF yield is LOWER — meaning the market
  is paying a PREMIUM for Apple's growth expectation.
  This is exactly why our DCF shows the stock as
  OVERVALUED at $308.82 vs. intrinsic value of $231.04.
    """)


cashflow_quality_analysis(analyzer)

