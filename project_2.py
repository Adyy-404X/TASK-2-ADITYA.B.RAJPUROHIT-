# PROGRAMMING CONCEPT: Classes
# A class is a blueprint for an object.
# Think of it like a "Financial Analyst Template" —
# you define it once, then create instances (analysts)
# that each work on different data.
#
# Our FinancialAnalyzer class will:
#   - Accept raw financial statements
#   - Perform horizontal & vertical analysis
#   - Generate formatted tables
#   - Create multi-year trend charts
# ============================================================

class FinancialAnalyzer:
    """
    A professional-grade financial statement analyzer.

    Usage:
        analyzer = FinancialAnalyzer(income_annual, balance_annual, cashflow_annual)
        analyzer.horizontal_analysis()
        analyzer.vertical_analysis()
    """

    def __init__(self, income_stmt, balance_sheet, cashflow_stmt):
        """
        Constructor — called when you create a new FinancialAnalyzer.
        __init__ runs automatically when you do: FinancialAnalyzer(...)
        'self' refers to THIS specific instance of the class.
        """
        self.income   = income_stmt    # Store income statement
        self.balance  = balance_sheet  # Store balance sheet
        self.cashflow = cashflow_stmt  # Store cash flow statement

        # Get column dates sorted oldest → newest for trend analysis
        # .columns gives us the time periods; sort so left = oldest
        self.periods = list(income_stmt.columns)
        # yfinance gives most recent FIRST — reverse for chronological order
        self.periods_sorted = sorted(self.periods)

        print("✅ FinancialAnalyzer initialized!")
        print(f"   Income Statement: {len(self.income.index)} line items")
        print(f"   Balance Sheet:    {len(self.balance.index)} line items")
        print(f"   Cash Flow:        {len(self.cashflow.index)} line items")
        print(f"\n   Time periods (oldest → newest):")
        for p in self.periods_sorted:
            print(f"     {p.strftime('%b %Y')}")

    def safe_get(self, df, keys, period, scale=1e9):
        """
        Safely extract a value by trying multiple key names.
        Returns 0 if nothing found (never crashes).
        """
        for key in keys:
            if key in df.index:
                try:
                    val = df.loc[key, period]
                    if pd.notna(val):
                        return float(val) / scale
                except:
                    continue
        return 0.0

    def pct_change(self, new_val, old_val):
        """
        Calculate percentage change from old to new value.
        Returns None if old value is 0 (can't divide by zero).
        """
        if old_val == 0 or pd.isna(old_val):
            return None
        return ((new_val - old_val) / abs(old_val)) * 100

    def color_pct(self, pct):
        """
        Return a text symbol indicating direction of change.
        ▲ = growth (good for revenue/profit)
        ▼ = decline
        — = no change / not available
        """
        if pct is None:
            return "   —  "
        elif pct > 0:
            return f" ▲{pct:+.1f}%"
        elif pct < 0:
            return f" ▼{pct:+.1f}%"
        else:
            return "  0.0%"


