# PROGRAMMING CONCEPT: NumPy Random Distributions
# Instead of fixed numbers, we sample from probability
# distributions — bell curves (normal) and bounded ranges
# (uniform) — to model real-world uncertainty.
#
# FINANCIAL CONCEPT: Why distributions?
# We don't KNOW Apple's exact growth rate next year.
# But we can say: "Probably 5-8%, unlikely below 2% or
# above 15%." A normal distribution captures this belief.
# ============================================================

import numpy as np

N_SIMULATIONS = 10_000   # Number of DCF runs
np.random.seed(42)       # Reproducibility (same "random" each run)

# ── PARAMETER DISTRIBUTIONS ────────────────────────────────
# Each parameter: (mean, std_dev) for normal distribution
# OR (low, high) for uniform distribution

MC_PARAMS = {
    # Revenue Growth — normally distributed around base case
    'growth_yr1_3': {
        'dist': 'normal',
        'mean': 0.065,
        'std':  0.025,   # ±2.5% uncertainty
        'min':  0.0,     # Can't have negative revenue (truncate)
        'max':  0.20,
    },
    'growth_yr4_5': {
        'dist': 'normal',
        'mean': 0.055,
        'std':  0.020,
        'min':  0.0,
        'max':  0.15,
    },
    'terminal_growth': {
        'dist': 'normal',
        'mean': 0.030,
        'std':  0.005,   # Tight — anchored to long-run GDP
        'min':  0.01,
        'max':  0.045,   # Never exceed WACC!
    },
    'fcf_margin': {
        'dist': 'normal',
        'mean': 0.265,
        'std':  0.020,   # ±2% margin uncertainty
        'min':  0.15,
        'max':  0.35,
    },
    'wacc': {
        'dist': 'normal',
        'mean': 0.092,
        'std':  0.008,   # ±0.8% discount rate uncertainty
        'min':  0.07,
        'max':  0.13,
    },
}

def sample_params(n):
    """
    Sample n sets of parameters from their distributions.
    Returns dict of arrays, each length n.
    Uses np.clip() to enforce min/max bounds (truncation).
    """
    samples = {}
    for param, config in MC_PARAMS.items():
        if config['dist'] == 'normal':
            raw = np.random.normal(config['mean'], config['std'], n)
        else:  # uniform
            raw = np.random.uniform(config['min'], config['max'], n)

        # Clip to bounds (truncated normal)
        samples[param] = np.clip(raw, config['min'], config['max'])

    return samples

