def interpret_ratios(ticker, ratios, peer_df):
    """
    Generates plain-English interpretation of a company's ratios
    vs. its peer group.
    """
    print("=" * 70)
    print(f"   {ticker} — RATIO INTERPRETATION REPORT")
    print("=" * 70)

    r = ratios  # Shorthand

    # ─── VALUATION VERDICT ─────────────────────────────────
    pe = r.get('P/E (TTM)', 0)
    fpe = r.get('Forward P/E', 0)
    peg = r.get('PEG Ratio', 0)

    # Peer average P/E
    peer_pes = [all_ratios[t].get('P/E (TTM)', 0)
                for t in PEERS if t != ticker and all_ratios.get(t, {}).get('P/E (TTM)', 0) > 0]
    avg_peer_pe = np.mean(peer_pes) if peer_pes else 0

    print(f"\n  📊 VALUATION ANALYSIS")
    print(f"  {'─'*60}")
    print(f"  Trailing P/E:  {pe:.1f}x  (Peer Avg: {avg_peer_pe:.1f}x)")

    premium = ((pe - avg_peer_pe) / avg_peer_pe * 100) if avg_peer_pe > 0 else 0
    if premium > 15:
        print(f"  → {ticker} trades at a {premium:.1f}% PREMIUM to peers.")
        print(f"    Market expects faster growth OR assigns brand/moat premium.")
        print(f"    RISK: If growth disappoints, multiple compression likely.")
    elif premium < -15:
        print(f"  → {ticker} trades at a {abs(premium):.1f}% DISCOUNT to peers.")
        print(f"    Potential value opportunity OR market sees a problem.")
    else:
        print(f"  → {ticker} trades roughly IN-LINE with peers ({premium:+.1f}%).")

    if fpe > 0 and pe > 0:
        if fpe < pe:
            print(f"\n  Forward P/E ({fpe:.1f}x) < Trailing P/E ({pe:.1f}x)")
            print(f"  → Analysts expect earnings to GROW next year. ✅")
        else:
            print(f"\n  Forward P/E ({fpe:.1f}x) > Trailing P/E ({pe:.1f}x)")
            print(f"  → Analysts expect earnings to DECLINE next year. ⚠️")

    if peg > 0:
        print(f"\n  PEG Ratio: {peg:.2f}")
        if peg < 1.0:
            print(f"  → PEG < 1.0: Stock may be UNDERVALUED relative to growth.")
        elif peg < 2.0:
            print(f"  → PEG 1-2: Fairly valued relative to growth expectations.")
        else:
            print(f"  → PEG > 2.0: Market paying a HIGH premium for growth.")

    # ─── PROFITABILITY VERDICT ──────────────────────────────
    gm  = r.get('Gross Margin %', 0)
    om  = r.get('Op Margin %', 0)
    nm  = r.get('Net Margin %', 0)
    roe = r.get('ROE %', 0)
    roa = r.get('ROA %', 0)

    peer_gms = [all_ratios[t].get('Gross Margin %', 0)
                for t in PEERS if t != ticker]
    avg_peer_gm = np.mean([v for v in peer_gms if v > 0])

    print(f"\n\n  💰 PROFITABILITY ANALYSIS")
    print(f"  {'─'*60}")
    print(f"  Gross Margin:     {gm:.1f}%   (Peer Avg: {avg_peer_gm:.1f}%)")
    print(f"  Operating Margin: {om:.1f}%")
    print(f"  Net Margin:       {nm:.1f}%")
    print(f"  ROE:              {roe:.1f}%")
    print(f"  ROA:              {roa:.1f}%")

    if gm > avg_peer_gm:
        print(f"\n  → Gross margin ABOVE peer average by {gm-avg_peer_gm:.1f}ppts.")
        print(f"    Reflects premium pricing power + high-margin Services mix.")
    else:
        print(f"\n  → Gross margin BELOW peer average by {avg_peer_gm-gm:.1f}ppts.")
        print(f"    May indicate more hardware-heavy revenue mix.")

    if roa > 15:
        print(f"\n  → ROA of {roa:.1f}% = exceptional asset efficiency.")
    elif roa > 5:
        print(f"\n  → ROA of {roa:.1f}% = solid, above most industries.")
    else:
        print(f"\n  → ROA of {roa:.1f}% = below average; check asset-heavy operations.")

    # ─── GROWTH VERDICT ─────────────────────────────────────
    rev_g = r.get('Revenue Growth %', 0)
    earn_g = r.get('Earnings Growth %', 0)

    print(f"\n\n  🚀 GROWTH ANALYSIS")
    print(f"  {'─'*60}")
    print(f"  Revenue Growth:  {rev_g:+.1f}% YoY")
    print(f"  Earnings Growth: {earn_g:+.1f}% YoY")

    if earn_g > rev_g:
        print(f"\n  → Earnings growing FASTER than revenue.")
        print(f"    Company is becoming more EFFICIENT (positive signal).")
    elif earn_g > 0:
        print(f"\n  → Both growing, revenue faster than earnings.")
        print(f"    Investing in growth — watch for margin expansion later.")
    else:
        print(f"\n  → Earnings declining. Investigate cause:")
        print(f"    Is it one-time (restructuring)? Or structural decline?")

    # ─── OVERALL VERDICT ────────────────────────────────────
    print(f"\n\n  📋 OVERALL INVESTMENT VERDICT FOR {ticker}")
    print(f"  {'─'*60}")

    # Simple scoring model
    score = 0
    if gm > avg_peer_gm:    score += 1
    if om > 25:             score += 1
    if rev_g > 5:           score += 1
    if earn_g > 5:          score += 1
    if roa > 10:            score += 1
    if peg > 0 and peg < 2: score += 1
    if roe > 15:            score += 1

    print(f"  Quality Score: {score}/7")
    if score >= 5:
        print(f"  Verdict: 🟢 HIGH QUALITY business. Premium valuation somewhat justified.")
    elif score >= 3:
        print(f"  Verdict: 🟡 AVERAGE quality. Look for better entry price.")
    else:
        print(f"  Verdict: 🔴 BELOW AVERAGE quality. Significant concerns exist.")

    print(f"\n  ⚠️  NOTE: This is ratio analysis only. Not a buy/sell recommendation.")
    print(f"  Full valuation requires DCF (Module 5) + Risk analysis (Module 7).")


