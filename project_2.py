# CELL 36: SENSITIVITY ANALYSIS
# "What if I'm wrong about WACC or terminal growth?"
#
# This 2D table shows intrinsic value for every combo of
# WACC (columns) and Terminal Growth Rate (rows).
# It's the standard output in any professional valuation.
# ============================================================

def sensitivity_analysis(base_revenue, current_price):
    """
    2D sensitivity table: WACC vs. Terminal Growth Rate.
    Shows intrinsic value at each combination.
    """
    print("\n" + "="*70)
    print("   SENSITIVITY ANALYSIS — WACC vs. Terminal Growth Rate")
    print("="*70)
    print("  Each cell = Intrinsic Value per Share ($)")
    print("  🟢 = Undervalued vs current price  |  "
          "🔴 = Overvalued  |  🟡 = Fair")
    print()

    wacc_range   = [0.080, 0.085, 0.090, 0.095, 0.100, 0.105]
    tgr_range    = [0.020, 0.025, 0.030, 0.035, 0.040]

    s = DCF_ASSUMPTIONS['base']  # Use base case structure

    # Header row
    header = f"  {'TGR \\ WACC':<12}"
    for w in wacc_range:
        header += f"  {w*100:.1f}%"
    print(header)
    print("  " + "─" * (12 + 7 * len(wacc_range)))

    for tgr in tgr_range:
        row = f"  {tgr*100:.1f}% TGR    "
        for wacc in wacc_range:
            # Quick DCF calculation (simplified)
            rev = base_revenue
            pv_sum = 0
            for yr in range(1, 6):
                g = s['growth_yr1_3'] if yr <= 3 else s['growth_yr4_5']
                rev *= (1 + g)
                fcf = rev * s['fcf_margin']
                pv_sum += fcf / (1 + wacc) ** yr

            # Terminal value
            fcf5 = base_revenue
            for yr in range(1, 6):
                g = s['growth_yr1_3'] if yr <= 3 else s['growth_yr4_5']
                fcf5 *= (1 + g)
            fcf5 *= s['fcf_margin']

            tv = (fcf5 * (1 + tgr)) / (wacc - tgr) / (1 + wacc) ** 5
            ev = pv_sum + tv - (-95.0)  # subtract net debt
            iv = ev / s['shares_out']

            # Color coding
            upside = (iv - current_price) / current_price
            if upside > 0.10:
                flag = '🟢'
            elif upside < -0.10:
                flag = '🔴'
            else:
                flag = '🟡'

            row += f" {flag}${iv:.0f}"
        print(row)

    print(f"""
  📌 HOW TO READ THIS TABLE:

  BASE CASE COORDINATES: WACC={s['wacc']*100:.1f}%, TGR={s['terminal_growth']*100:.1f}%
  Look at that cell — it matches our Base Case intrinsic value.

  KEY INSIGHT:
  Moving LEFT (lower WACC) = higher intrinsic value
  Moving DOWN (higher TGR)  = higher intrinsic value
  The model is most sensitive to the BOTTOM-LEFT corner.

  WHY THIS MATTERS:
  A good analyst doesn't give ONE number. They show a RANGE
  and explain which assumptions drive that range.
  This table is slide 1 of any real valuation presentation.
    """)


