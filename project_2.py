# CELL 12: BUILD A HELPER FUNCTION
#
# We'll need to safely extract values from financial statements
# many times throughout the project. Instead of repeating code,
# we write ONE function and call it whenever we need it.
#
# PROGRAMMING CONCEPT: DRY = "Don't Repeat Yourself"
# If you find yourself writing the same code 3+ times,
# turn it into a function.
# ============================================================

def get_value(dataframe, possible_keys, period_index=0, scale=1e9):
    """
    Safely extract a value from a financial statement DataFrame.

    Parameters:
    -----------
    dataframe     : The pandas DataFrame (income_stmt, balance_sheet, etc.)
    possible_keys : A list of possible row names to try (in priority order)
                    Different yfinance versions use different names!
    period_index  : Which time period column to use (0 = most recent)
    scale         : Divide by this number (1e9 = convert to Billions)

    Returns:
    --------
    float : The value divided by scale, or 0.0 if not found

    Example:
    --------
    get_value(income_annual, ['Total Revenue', 'Revenue'], 0, 1e9)
    → Returns revenue in Billions for the most recent annual period
    """
    for key in possible_keys:
        # Check if this key exists as a row in the DataFrame
        if key in dataframe.index:
            try:
                # .iloc[row, column] accesses by position
                # We search by row NAME first, then use column position
                value = dataframe.loc[key].iloc[period_index]

                # Check if it's a valid number (not NaN = "Not a Number")
                # NaN appears when data is missing
                if pd.notna(value) and value != 0:
                    return float(value) / scale  # Convert to desired scale
            except Exception:
                continue  # If this key fails, try the next one

    return 0.0  # Return 0 if nothing found (never crash!)


def format_billions(value):
    """Format a number as Billions with 2 decimal places."""
    return f"${value:.2f}B"

def format_percent(value):
    """Format a number as a percentage with 1 decimal place."""
    return f"{value:.1f}%"


