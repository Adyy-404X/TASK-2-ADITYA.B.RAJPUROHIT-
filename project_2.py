# CELL 27: THE RATIO CALCULATOR CLASS
#
# We'll build a class that:
# 1. Takes a company's info dictionary (from yfinance)
# 2. Extracts all key ratios safely
# 3. Returns a clean dictionary of ratios
#
# This is REUSABLE — we call it for each of the 5 companies
# and get a comparable set of numbers.
# ============================================================

class RatioCalculator:
    """
    Calculates and stores all key financial ratios for one company.

    Usage:
        calc = RatioCalculator('AAPL', info_dict)
        ratios = calc.get_all_ratios()
    """

    def __init__(self, ticker, info):
        self.ticker = ticker
        self.info   = info
        self.name   = info.get('longName', ticker)

    def safe_info(self, key, default=0):
        """Safely get a value from the info dict."""
        val = self.info.get(key, default)
        return val if val is not None else default

    # ─── VALUATION RATIOS ─────────────────────────────────────

    def pe_ratio(self):
        """
        Price-to-Earnings Ratio = Stock Price ÷ EPS
        HOW TO READ: P/E of 37x means investors pay $37 for
        every $1 of annual earnings. Higher P/E = higher
        growth expectations baked in.
        RULE OF THUMB: P/E > 30 = growth stock premium.
        """
        return self.safe_info('trailingPE')

    def forward_pe(self):
        """
        Forward P/E = Current Price ÷ Next Year's Estimated EPS
        More useful than Trailing P/E for fast-growing companies.
        Lower Forward P/E than Trailing P/E → earnings expected to GROW.
        """
        return self.safe_info('forwardPE')

    def pb_ratio(self):
        """
        Price-to-Book = Market Cap ÷ Book Value (Shareholders' Equity)
        HOW TO READ: P/B of 50x means investors pay $50 for
        every $1 of net assets. Very high P/B is NORMAL for
        asset-light businesses like Apple (brand value isn't
        on the balance sheet!).
        """
        return self.safe_info('priceToBook')

    def ps_ratio(self):
        """
        Price-to-Sales = Market Cap ÷ Annual Revenue
        Useful when P/E is not available (loss-making companies).
        Also a reality check: can the market cap be justified
        by revenue alone?
        """
        return self.safe_info('priceToSalesTrailing12Months')

    def ev_to_ebitda(self):
        """
        EV/EBITDA = Enterprise Value ÷ EBITDA
        Enterprise Value = Market Cap + Debt - Cash
        (the "true" acquisition cost of the business)
        EBITDA = Earnings Before Interest, Tax, Depreciation, Amortization
        WHY EBITDA? It strips out financing and accounting differences
        to compare operational performance across companies.
        """
        return self.safe_info('enterpriseToEbitda')

    def ev_to_revenue(self):
        """EV ÷ Revenue. Like P/S but uses enterprise value."""
        return self.safe_info('enterpriseToRevenue')

    def peg_ratio(self):
        """
        PEG Ratio = P/E ÷ EPS Growth Rate
        A BETTER valuation metric than P/E alone.
        PEG < 1.0 = potentially undervalued (cheap given growth)
        PEG > 2.0 = potentially overvalued (expensive given growth)
        PEG = 1.0 = "fair value" (paying exactly for growth)
        """
        return self.safe_info('pegRatio')

    # ─── PROFITABILITY RATIOS ─────────────────────────────────

    def gross_margin(self):
        """(Revenue - COGS) ÷ Revenue × 100. Apple ~49%."""
        return self.safe_info('grossMargins', 0) * 100

    def operating_margin(self):
        """Operating Income ÷ Revenue × 100. Apple ~32%."""
        return self.safe_info('operatingMargins', 0) * 100

    def net_margin(self):
        """Net Income ÷ Revenue × 100. Apple ~26%."""
        return self.safe_info('profitMargins', 0) * 100

    def roe(self):
        """
        Return on Equity = Net Income ÷ Shareholders' Equity × 100
        HOW TO READ: How much profit Apple generates per $1
        of shareholder money invested.
        Apple's ROE is extremely high (often >100%) due to
        the same buyback effect that inflates D/E.
        RULE OF THUMB: ROE > 15% = strong business.
        """
        return self.safe_info('returnOnEquity', 0) * 100

    def roa(self):
        """
        Return on Assets = Net Income ÷ Total Assets × 100
        Unlike ROE, ROA is not distorted by leverage.
        Better for comparing companies with different debt levels.
        RULE OF THUMB: ROA > 5% = efficient use of assets.
        """
        return self.safe_info('returnOnAssets', 0) * 100

    # ─── GROWTH METRICS ───────────────────────────────────────

    def revenue_growth(self):
        """YoY Revenue Growth %. Positive = growing top line."""
        return self.safe_info('revenueGrowth', 0) * 100

    def earnings_growth(self):
        """YoY Earnings (Net Income) Growth %."""
        return self.safe_info('earningsGrowth', 0) * 100

    # ─── DIVIDEND & BUYBACK ───────────────────────────────────

    def dividend_yield(self):
        """Annual dividend ÷ stock price × 100. Apple ~0.5%."""
        return self.safe_info('dividendYield', 0) * 100

    def payout_ratio(self):
        """Dividends paid ÷ Net Income × 100."""
        return self.safe_info('payoutRatio', 0) * 100

    # ─── COMPILE ALL ──────────────────────────────────────────

    def get_all_ratios(self):
        """Returns a dict of all ratios for this company."""
        return {
            'Company':          self.name,
            # Valuation
            'P/E (TTM)':        self.pe_ratio(),
            'Forward P/E':      self.forward_pe(),
            'P/B Ratio':        self.pb_ratio(),
            'P/S Ratio':        self.ps_ratio(),
            'EV/EBITDA':        self.ev_to_ebitda(),
            'PEG Ratio':        self.peg_ratio(),
            # Profitability
            'Gross Margin %':   self.gross_margin(),
            'Op Margin %':      self.operating_margin(),
            'Net Margin %':     self.net_margin(),
            'ROE %':            self.roe(),
            'ROA %':            self.roa(),
            # Growth
            'Revenue Growth %': self.revenue_growth(),
            'Earnings Growth %':self.earnings_growth(),
            # Shareholder Returns
            'Dividend Yield %': self.dividend_yield(),
            'Payout Ratio %':   self.payout_ratio(),
        }


