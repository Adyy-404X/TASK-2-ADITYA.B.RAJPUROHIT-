# Imagine a store that sold $1M of goods on CREDIT —
# they show $1M profit, but have $0 cash in hand.
#
# The Cash Flow Statement tracks REAL CASH MOVEMENT.
# It has THREE sections:
#
# 1. Operating Cash Flow (OCF)
#    Cash generated from the CORE BUSINESS
#    (selling iPhones, subscription fees, etc.)
#    This is the most important number for valuation!
#
# 2. Investing Cash Flow
#    Cash spent on GROWING the business
#    (buying equipment, factories, acquisitions)
#    Usually NEGATIVE (spending to grow)
#
# 3. Financing Cash Flow
#    Cash from/to INVESTORS and LENDERS
#    (issuing bonds, buying back shares, paying dividends)
#    Usually NEGATIVE (returning money to shareholders)
#
# FREE CASH FLOW = Operating CF - Capital Expenditures (CapEx)
# This is the "true" cash profit available for investors.
# ============================================================

