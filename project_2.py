# ============================================================
# CELL 19: HORIZONTAL ANALYSIS
#
# WHAT IT IS: Compare each line item year-over-year (YoY).
# "Did Apple's revenue grow or shrink vs. the prior year?"
#
# FORMULA: % Change = (New Value - Old Value) / |Old Value| × 100
#
# Example:
#   FY2024 Revenue = $391.04B
#   FY2023 Revenue = $383.29B
#   YoY Growth = (391.04 - 383.29) / 383.29 × 100 = +2.02%
#
# Why it matters:
#   A company with SHRINKING revenue but GROWING profit
#   is becoming more efficient (good signal).
#   A company with GROWING revenue but SHRINKING profit
#   is losing pricing power (bad signal).
# ============================================================

def horizontal_analysis(analyzer):
    """Perform year-over-year growth analysis on income statement."""

    print("=" * 80)
    print("         APPLE INC. — HORIZONTAL ANALYSIS (Year-over-Year Growth)")
    print("=" * 80)

    # The key line items we care about (in order they appear on the P&L)
    line_items = [
        {
            'label': 'Total Revenue',
            'keys':  ['Total Revenue', 'Revenue'],
            'emoji': '💰'
        },
        {
            'label': 'Gross Profit',
            'keys':  ['Gross Profit', 'GrossProfit'],
            'emoji': '📈'
        },
        {
            'label': 'Operating Income',
            'keys':  ['Operating Income', 'EBIT'],
            'emoji': '🏭'
        },
        {
            'label': 'Net Income',
            'keys':  ['Net Income', 'Net Income Common Stockholders'],
            'emoji': '💵'
        },
        {
            'label': 'R&D Expense',
            'keys':  ['Research And Development', 'Research Development'],
            'emoji': '🔬'
        },
    ]

    periods = analyzer.periods_sorted  # Oldest to newest

    # Build header row — one column per year
    header = f"  {'Metric':<25}"
    for p in periods:
        header += f"  {p.strftime('%b %Y'):>10}"
    header += f"  {'2Y CAGR':>8}"
    print(header)
    print("  " + "─" * 75)

    # For each metric, print values across years AND the YoY % change
    for item in line_items:
        values = {}
        for p in periods:
            values[p] = analyzer.safe_get(analyzer.income, item['keys'], p, scale=1e9)

        # Row 1: Absolute values in $Billions
        value_row = f"  {item['emoji']} {item['label']:<23}"
        for p in periods:
            v = values[p]
            value_row += f"  {f'${v:.1f}B':>10}"

        # Compute 2-year CAGR (Compound Annual Growth Rate)
        # CAGR = (End Value / Start Value)^(1/n) - 1
        # where n = number of years
        if len(periods) >= 2:
            start_val = values[periods[0]]
            end_val   = values[periods[-1]]
            n_years   = len(periods) - 1
            if start_val > 0 and end_val > 0:
                cagr = ((end_val / start_val) ** (1 / n_years) - 1) * 100
                value_row += f"  {f'{cagr:+.1f}%':>8}"
            else:
                value_row += f"  {'N/A':>8}"

        print(value_row)

        # Row 2: YoY % change (skip the first period — no prior year)
        change_row = f"  {'  YoY Change':<25}"
        for i, p in enumerate(periods):
            if i == 0:
                change_row += f"  {'(base)':>10}"
            else:
                prev_p = periods[i - 1]
                pct = analyzer.pct_change(values[p], values[prev_p])
                change_row += f"  {analyzer.color_pct(pct):>10}"
        change_row += f"  {'':>8}"
        print(change_row)
        print()

    print("""
  📌 HOW TO READ THIS TABLE:

  ▲ = Growth (positive YoY change)  |  ▼ = Decline (negative YoY change)

  WHAT TO LOOK FOR:
  ✅ Revenue growing AND Net Income growing faster → improving efficiency
  ✅ R&D growing → investing in future products (Apple Intelligence, etc.)
  ⚠️  Revenue flat or declining → competitive pressure or market saturation
  ⚠️  Net Income growing FASTER than Revenue → could be one-time items
    """)


# Run it!
horizontal_analysis(analyzer)

# ============================================================
# CELL 20: VERTICAL ANALYSIS — COMMON-SIZE STATEMENTS
#
# WHAT IT IS: Express every line item as a % of Revenue.
# This makes companies of different sizes COMPARABLE.
#
# EXAMPLE:
#   Apple Revenue = $391B  →  Gross Profit = $181B → 46.2% of Revenue
#   Samsung Revenue = $200B → Gross Profit = $60B  → 30.0% of Revenue
#
# Even though Apple earns more in dollars, the PERCENTAGE
# tells us Apple is fundamentally more efficient.
#
# This is called a "Common-Size" statement.
# ============================================================

def vertical_analysis(analyzer):
    """Express each income statement line as % of Revenue."""

    print("=" * 80)
    print("         APPLE INC. — VERTICAL ANALYSIS (Common-Size P&L)")
    print("=" * 80)
    print("  Each value = Line Item ÷ Total Revenue × 100")
    print()

    # Items to analyze as % of Revenue
    items = [
        ('Total Revenue',    ['Total Revenue', 'Revenue'],              '💰', '(= 100% by definition)'),
        ('Cost of Revenue',  ['Cost Of Revenue', 'Cost of Revenue'],    '🏭', '(Production cost %)'),
        ('Gross Profit',     ['Gross Profit', 'GrossProfit'],           '📊', '(= Gross Margin %)'),
        ('R&D Expense',      ['Research And Development'],              '🔬', '(Innovation spend %)'),
        ('SG&A Expense',     ['Selling General Administrative',
                               'Selling General And Administration'],   '📣', '(Overhead %)'),
        ('Operating Income', ['Operating Income', 'EBIT'],              '⚙️', '(= Operating Margin %)'),
        ('Net Income',       ['Net Income',
                               'Net Income Common Stockholders'],       '💵', '(= Net Margin %)'),
    ]

    periods = analyzer.periods_sorted

    # Header
    header = f"  {'Metric':<28}"
    for p in periods:
        header += f"  {p.strftime('%b %Y'):>9}"
    print(header)
    print("  " + "─" * 68)

    for label, keys, emoji, note in items:
        row = f"  {emoji} {label:<26}"
        for p in periods:
            # Get revenue for this period (denominator)
            revenue = analyzer.safe_get(
                analyzer.income, ['Total Revenue', 'Revenue'], p, scale=1e9)
            # Get this item's value
            value = analyzer.safe_get(analyzer.income, keys, p, scale=1e9)

            if revenue > 0:
                pct = (value / revenue) * 100
                row += f"  {pct:>7.1f}%"
            else:
                row += f"  {'N/A':>8}"
        row += f"  {note}"
        print(row)

    print(f"""
  📌 WHAT APPLE'S MARGINS TELL US:

  Gross Margin ~46-49%:
    Apple keeps ~48¢ of every $1 of sales after manufacturing costs.
    Consumer electronics industry average is only ~20-30%.
    WHY? Apple sells SOFTWARE, SERVICES, and premium hardware.
    The App Store, iCloud, Apple Music have near-ZERO marginal cost.

  Operating Margin ~29-32%:
    After paying all employees, R&D, and marketing.
    This is exceptional for a company doing $391B in revenue.

  Net Margin ~24-27%:
    Apple keeps ~26¢ of every $1 of revenue as final profit.
    For context: Walmart's net margin is ~2-3%.

  TREND TO WATCH:
    If Services (App Store, Apple TV+, iCloud) keep growing,
    margins should EXPAND further — services are high-margin.
    """)


vertical_analysis(analyzer)

# ============================================================
# CELL 21: MULTI-YEAR TREND VISUALIZATION
#
# Charts reveal what tables hide.
# We'll create a 2-panel chart:
#   Top panel:    Revenue & Gross Profit (bar chart)
#   Bottom panel: All three margin % (line chart)
#
# This is the chart you'd put in an investment memo.
# ============================================================

def plot_financial_trends(analyzer):
    """Create professional multi-year financial trend charts."""

    periods = analyzer.periods_sorted
    period_labels = [p.strftime('%b\n%Y') for p in periods]

    # ---- EXTRACT DATA FOR PLOTTING ----
    revenues       = [analyzer.safe_get(analyzer.income, ['Total Revenue', 'Revenue'], p) for p in periods]
    gross_profits  = [analyzer.safe_get(analyzer.income, ['Gross Profit', 'GrossProfit'], p) for p in periods]
    op_incomes     = [analyzer.safe_get(analyzer.income, ['Operating Income', 'EBIT'], p) for p in periods]
    net_incomes    = [analyzer.safe_get(analyzer.income, ['Net Income', 'Net Income Common Stockholders'], p) for p in periods]

    # Calculate margin percentages
    gross_margins  = [(gp/r*100) if r > 0 else 0 for gp, r in zip(gross_profits, revenues)]
    op_margins     = [(op/r*100) if r > 0 else 0 for op, r in zip(op_incomes, revenues)]
    net_margins    = [(ni/r*100) if r > 0 else 0 for ni, r in zip(net_incomes, revenues)]

    # ---- SET UP FIGURE WITH 2 SUBPLOTS ----
    # 2, 1 means 2 rows, 1 column
    # figsize = width, height in inches
    fig, (ax1, ax2) = plt.subplots(2, 1, figsize=(14, 10))
    fig.suptitle('Apple Inc. (AAPL) — 5-Year Financial Performance',
                 fontsize=16, fontweight='bold', y=0.98)

    # ---- PANEL 1: REVENUE & PROFITS (BAR CHART) ----
    x = np.arange(len(periods))   # [0, 1, 2, 3] — positions on x-axis
    width = 0.25                   # Width of each bar

    # Plot three sets of bars side by side
    bars1 = ax1.bar(x - width,     revenues,      width, label='Revenue',        color='#2196F3', alpha=0.85)
    bars2 = ax1.bar(x,             gross_profits, width, label='Gross Profit',   color='#4CAF50', alpha=0.85)
    bars3 = ax1.bar(x + width,     net_incomes,   width, label='Net Income',     color='#FF9800', alpha=0.85)

    # Add value labels ON TOP of each bar
    def add_bar_labels(ax, bars):
        for bar in bars:
            height = bar.get_height()
            if height > 0:
                ax.annotate(f'${height:.0f}B',
                            xy=(bar.get_x() + bar.get_width() / 2, height),
                            xytext=(0, 3),   # 3 points above bar
                            textcoords='offset points',
                            ha='center', va='bottom', fontsize=8)

    add_bar_labels(ax1, bars1)
    add_bar_labels(ax1, bars2)
    add_bar_labels(ax1, bars3)

    ax1.set_xlabel('')
    ax1.set_ylabel('USD (Billions)', fontsize=11)
    ax1.set_title('Annual Revenue, Gross Profit & Net Income', fontsize=12, pad=10)
    ax1.set_xticks(x)
    ax1.set_xticklabels(period_labels, fontsize=9)
    ax1.legend(loc='upper left', fontsize=10)
    ax1.grid(True, axis='y', alpha=0.3)
    ax1.set_facecolor('#f8f9fa')

    # ---- PANEL 2: MARGIN TRENDS (LINE CHART) ----
    ax2.plot(period_labels, gross_margins, 'o-', color='#4CAF50',
             linewidth=2.5, markersize=8, label='Gross Margin %')
    ax2.plot(period_labels, op_margins,    's-', color='#2196F3',
             linewidth=2.5, markersize=8, label='Operating Margin %')
    ax2.plot(period_labels, net_margins,   '^-', color='#FF9800',
             linewidth=2.5, markersize=8, label='Net Margin %')

    # Add data labels on each data point
    for i, (gm, om, nm) in enumerate(zip(gross_margins, op_margins, net_margins)):
        ax2.annotate(f'{gm:.1f}%', (period_labels[i], gm),
                     textcoords='offset points', xytext=(0, 8),
                     ha='center', fontsize=8, color='#4CAF50', fontweight='bold')
        ax2.annotate(f'{om:.1f}%', (period_labels[i], om),
                     textcoords='offset points', xytext=(0, -15),
                     ha='center', fontsize=8, color='#2196F3')
        ax2.annotate(f'{nm:.1f}%', (period_labels[i], nm),
                     textcoords='offset points', xytext=(0, 8),
                     ha='center', fontsize=8, color='#FF9800')

    ax2.set_ylabel('Margin (%)', fontsize=11)
    ax2.set_title('Profitability Margins — 5-Year Trend', fontsize=12, pad=10)
    ax2.legend(loc='lower right', fontsize=10)
    ax2.grid(True, alpha=0.3)
    ax2.set_facecolor('#f8f9fa')
    ax2.set_ylim(0, 70)   # Y-axis from 0% to 70%

    plt.tight_layout()

    # Save to Google Drive
    chart_path = f"{PATHS['charts']}/AAPL_financial_trends.png"
    plt.savefig(chart_path, dpi=150, bbox_inches='tight')
    plt.show()
    print(f"\n💾 Trend chart saved to: {chart_path}")


plot_financial_trends(analyzer)

