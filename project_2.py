# CELL 29: RATIO INTERPRETATION ENGINE
#
# Raw numbers are data. INTERPRETED numbers are insight.
# This cell automatically interprets Apple's ratios and
# generates the kind of commentary you'd see in a
# Goldman Sachs equity research note.
