# ============================================================
# CELL 34: DCF ENGINE — THE CORE CALCULATION
#
# STEP BY STEP:
# 1. Project Revenue for 5 years using growth rates
# 2. Apply FCF margin to get Free Cash Flow each year
# 3. Discount each FCF back to today using WACC
# 4. Calculate Terminal Value (the value AFTER year 5)
# 5. Sum everything → Enterprise Value
# 6. Subtract net debt → Equity Value
# 7. Divide by shares → Intrinsic Value per Share
# ============================================================

def run_dcf(scenario_key, base_revenue, current_price):
    """
    Full DCF model for one scenario.
    Returns dict with all intermediate steps + final value.
    """
    s = DCF_ASSUMPTIONS[scenario_key]

    print(f"\n{'='*70}")
    print(f"  {s['emoji']} {s['label'].upper()} DCF MODEL")
    print(f"{'='*70}")

    # ── STEP 1: PROJECT REVENUE ─────────────────────────────
    revenues = []
    fcfs     = []
    rev = base_revenue   # Starting point (most recent annual)

    print(f"\n  STEP 1 & 2: Revenue & FCF Projections")
    print(f"  {'Year':<6} {'Growth':>8} {'Revenue ($B)':>14} "
          f"{'FCF Margin':>12} {'FCF ($B)':>10}")
    print(f"  {'─'*55}")

    for yr in range(1, 6):
        # Apply growth rate based on phase
        if yr <= 3:
            growth = s['growth_yr1_3']
        else:
            growth = s['growth_yr4_5']

        rev = rev * (1 + growth)
        fcf = rev * s['fcf_margin']
        revenues.append(rev)
        fcfs.append(fcf)

        print(f"  Yr {yr:<3} {growth*100:>7.1f}%  "
              f"${rev:>11.2f}B  "
              f"{s['fcf_margin']*100:>10.1f}%  "
              f"${fcf:>8.2f}B")

    # ── STEP 3: DISCOUNT EACH FCF ───────────────────────────
    wacc = s['wacc']
    pv_fcfs = []

    print(f"\n  STEP 3: Discount FCFs at WACC = {wacc*100:.1f}%")
    print(f"  {'Year':<6} {'FCF ($B)':>10} {'Discount Factor':>16} "
          f"{'PV of FCF ($B)':>15}")
    print(f"  {'─'*52}")

    for yr, fcf in enumerate(fcfs, 1):
        discount_factor = 1 / (1 + wacc) ** yr
        pv = fcf * discount_factor
        pv_fcfs.append(pv)
        print(f"  Yr {yr:<3} ${fcf:>8.2f}B  "
              f"{discount_factor:>14.4f}  "
              f"${pv:>13.2f}B")

    sum_pv_fcfs = sum(pv_fcfs)
    print(f"\n  Sum of PV(FCFs): ${sum_pv_fcfs:.2f}B")

    # ── STEP 4: TERMINAL VALUE ──────────────────────────────
    # Gordon Growth Model:
    # TV = FCF_year5 × (1 + g) / (WACC - g)
    # Then discount back 5 years
    g  = s['terminal_growth']
    tv_numerator   = fcfs[-1] * (1 + g)
    tv_denominator = wacc - g
    terminal_value = tv_numerator / tv_denominator
    pv_terminal    = terminal_value / (1 + wacc) ** 5

    print(f"\n  STEP 4: Terminal Value (Gordon Growth Model)")
    print(f"  Terminal Growth Rate:  {g*100:.1f}%")
    print(f"  Year 5 FCF:           ${fcfs[-1]:.2f}B")
    print(f"  TV = FCF5×(1+g)/(WACC-g) = ${terminal_value:.2f}B")
    print(f"  PV of Terminal Value:  ${pv_terminal:.2f}B")

    # ── STEP 5: ENTERPRISE VALUE ────────────────────────────
    enterprise_value = sum_pv_fcfs + pv_terminal
    tv_pct = (pv_terminal / enterprise_value) * 100

    print(f"\n  STEP 5: Enterprise Value")
    print(f"  PV of FCFs:       ${sum_pv_fcfs:.2f}B  "
          f"({100-tv_pct:.1f}% of total)")
    print(f"  PV Terminal Value:${pv_terminal:.2f}B  "
          f"({tv_pct:.1f}% of total)")
    print(f"  Enterprise Value: ${enterprise_value:.2f}B")

    # ── STEP 6: EQUITY VALUE (subtract net debt) ────────────
    # Net Debt = Total Debt - Cash
    # We pull from balance sheet or use known values
    try:
        most_recent = analyzer.periods_sorted[-1]
        cash = analyzer.safe_get(
            analyzer.balance,
            ['Cash And Cash Equivalents',
             'Cash Cash Equivalents And Short Term Investments'],
            most_recent)
        debt = analyzer.safe_get(
            analyzer.balance,
            ['Total Debt', 'Long Term Debt'],
            most_recent)
        net_debt = debt - cash
    except:
        # Apple FY2024 fallback: ~$67B debt - ~$162B cash = -$95B
        net_debt = -95.0

    equity_value = enterprise_value - net_debt

    print(f"\n  STEP 6: Bridge to Equity Value")
    print(f"  Enterprise Value: ${enterprise_value:.2f}B")
    print(f"  Net Debt (Debt-Cash): ${net_debt:.2f}B")
    print(f"  Equity Value:     ${equity_value:.2f}B")

    # ── STEP 7: INTRINSIC VALUE PER SHARE ───────────────────
    shares = s['shares_out']
    intrinsic_value = equity_value / shares

    upside = ((intrinsic_value - current_price) / current_price) * 100
    verdict = ('UNDERVALUED 🟢' if upside > 10
               else 'OVERVALUED 🔴' if upside < -10
               else 'FAIRLY VALUED 🟡')

    print(f"\n  STEP 7: Intrinsic Value Per Share")
    print(f"  Equity Value:     ${equity_value:.2f}B")
    print(f"  Shares Out:       {shares:.1f}B")
    print(f"  ┌─────────────────────────────────────────┐")
    print(f"  │  INTRINSIC VALUE:  ${intrinsic_value:>7.2f} per share  │")
    print(f"  │  CURRENT PRICE:    ${current_price:>7.2f} per share  │")
    print(f"  │  UPSIDE/DOWNSIDE:  {upside:>+7.1f}%               │")
    print(f"  │  VERDICT:          {verdict:<22}│")
    print(f"  └─────────────────────────────────────────┘")

    return {
        'scenario':       s['label'],
        'intrinsic_value': intrinsic_value,
        'upside_pct':     upside,
        'enterprise_value': enterprise_value,
        'pv_fcfs':        sum_pv_fcfs,
        'pv_terminal':    pv_terminal,
        'tv_pct':         tv_pct,
        'revenues':       revenues,
        'fcfs':           fcfs,
        'pv_fcfs_list':   pv_fcfs,
        'verdict':        verdict,
    }


