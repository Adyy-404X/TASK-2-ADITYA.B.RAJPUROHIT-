# CELL 22: BALANCE SHEET STRENGTH — MULTI-YEAR VIEW
#
# A STRONG balance sheet has:
# ✅ Growing assets
# ✅ Manageable debt (or growing cash faster than debt)
# ✅ Positive and growing equity
#
# Apple's UNUSUAL situation:
# Their equity has been SHRINKING — not because the business
# is weakening, but because they're buying back so many shares
# (returning cash to shareholders) that equity goes DOWN.
# This is actually SHAREHOLDER-FRIENDLY behavior.
# ============================================================

def balance_sheet_analysis(analyzer):
    """Multi-year balance sheet strength analysis."""

    print("=" * 75)
    print("         APPLE INC. — BALANCE SHEET TREND ANALYSIS")
    print("=" * 75)

    periods = analyzer.periods_sorted

    # Extract balance sheet items across all years
    total_assets_list   = [analyzer.safe_get(analyzer.balance, ['Total Assets'], p) for p in periods]
    total_liab_list     = [analyzer.safe_get(analyzer.balance,
                            ['Total Liabilities Net Minority Interest', 'Total Liabilities'], p)
                           for p in periods]
    equity_list         = [analyzer.safe_get(analyzer.balance,
                            ['Stockholders Equity', 'Common Stock Equity',
                             'Total Equity Gross Minority Interest'], p)
                           for p in periods]
    cash_list           = [analyzer.safe_get(analyzer.balance,
                            ['Cash And Cash Equivalents',
                             'Cash Cash Equivalents And Short Term Investments'], p)
                           for p in periods]
    debt_list           = [analyzer.safe_get(analyzer.balance,
                            ['Total Debt', 'Long Term Debt'], p)
                           for p in periods]

    # Calculate derived metrics
    net_cash_list = [c - d for c, d in zip(cash_list, debt_list)]
    de_ratio_list = [(d / e * 100) if e != 0 else 0 for d, e in zip(debt_list, equity_list)]
    current_ratio_list = []
    for p in periods:
        curr_assets = analyzer.safe_get(analyzer.balance, ['Current Assets', 'Total Current Assets'], p)
        curr_liab   = analyzer.safe_get(analyzer.balance,
                        ['Current Liabilities', 'Total Current Liabilities',
                         'Current Liabilities Net Minority Interest'], p)
        cr = curr_assets / curr_liab if curr_liab > 0 else 0
        current_ratio_list.append(cr)

    # Print table
    period_labels = [p.strftime('%b %Y') for p in periods]
    header = f"\n  {'Metric':<28}" + "".join(f"  {lbl:>10}" for lbl in period_labels)
    print(header)
    print("  " + "─" * (28 + 12 * len(periods)))

    rows = [
        ('💼 Total Assets ($B)',    total_assets_list,   '${:.1f}B'),
        ('📉 Total Liabilities ($B)', total_liab_list,  '${:.1f}B'),
        ('🏦 Shareholders Equity ($B)', equity_list,    '${:.1f}B'),
        ('💵 Cash & Equiv. ($B)',    cash_list,          '${:.1f}B'),
        ('💳 Total Debt ($B)',       debt_list,          '${:.1f}B'),
        ('🟢 Net Cash ($B)',         net_cash_list,      '${:.1f}B'),
        ('📐 D/E Ratio (%)',         de_ratio_list,      '{:.1f}%'),
        ('📊 Current Ratio',         current_ratio_list, '{:.2f}x'),
    ]

    for label, values, fmt in rows:
        row = f"  {label:<28}"
        for v in values:
            try:
                row += f"  {fmt.format(v):>10}"
            except:
                row += f"  {'N/A':>10}"
        print(row)

    print(f"""
  📌 KEY INSIGHTS FROM BALANCE SHEET TRENDS:

  1. SHRINKING EQUITY is not a red flag for Apple.
     Cause: Aggressive share buybacks reduce equity mathematically.
     Apple repurchased $95B+ in shares in FY2024 alone.
     This is a CAPITAL RETURN program, not financial distress.

  2. NET CASH = the safety net.
     As long as Cash > Debt, Apple can handle any downturn.
     Watch this number — it's the true measure of financial health.

  3. CURRENT RATIO measures short-term liquidity:
     Current Ratio = Current Assets ÷ Current Liabilities
     > 1.0 = can pay near-term bills ✅
     < 1.0 = might need to borrow for short-term needs ⚠️
     Apple often runs < 1.0 intentionally — they have predictable
     cash flows and can issue bonds cheaply if needed.
    """)


balance_sheet_analysis(analyzer)

